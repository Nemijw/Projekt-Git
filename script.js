document.getElementById("bulb1").addEventListener("click", function(){
    var bulb1img = document.getElementById("bulb1img");
    if(bulb1img.style.display == "none"){
        bulb1img.style.display = "block";
    }else{
        bulb1img.style.display = "none";
    }
});

